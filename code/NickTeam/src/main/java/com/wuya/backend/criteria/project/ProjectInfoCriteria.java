package com.wuya.backend.criteria.project;

import com.wuya.base.criteria.BaseCriteria;

/**
 * [项目－条件封装]
 * 
 * @author nick
 * @version v1.0 2015-2-3
 */
public class ProjectInfoCriteria extends BaseCriteria {

	/**
	 * [对象序列化标识]
	 */
	private static final long serialVersionUID = -8549262617291383913L;

	/**
	 * [项目名－like匹配]
	 */
	private String lname;

	/**
	 * [父及标识]
	 */
	private Integer pid;

	/**
	 * [项目等级]
	 */
	private Integer level;

	/**
	 * [是否上线]
	 */
	private String isOnline;

	// ============== 标志性字段 开始 ================== //

	/**
	 * [是否需要子项目]
	 */
	private String needSubProject;

	/**
	 * [是否需要加载费用]
	 */
	private String needCost;

	/**
	 * [获取图片]
	 */
	private String needImage;

	// ============== 标志性字段 结束 ================== //

	public String getLname() {
		return lname;
	}

	public void setLname(String lname) {
		this.lname = lname;
	}

	public Integer getPid() {
		return pid;
	}

	public void setPid(Integer pid) {
		this.pid = pid;
	}

	public Integer getLevel() {
		return level;
	}

	public void setLevel(Integer level) {
		this.level = level;
	}

	public String getIsOnline() {
		return isOnline;
	}

	public void setIsOnline(String isOnline) {
		this.isOnline = isOnline;
	}

	public String getNeedSubProject() {
		return needSubProject;
	}

	public void setNeedSubProject(String needSubProject) {
		this.needSubProject = needSubProject;
	}

	public String getNeedCost() {
		return needCost;
	}

	public void setNeedCost(String needCost) {
		this.needCost = needCost;
	}

	public String getNeedImage() {
		return needImage;
	}

	public void setNeedImage(String needImage) {
		this.needImage = needImage;
	}

	@Override
	public String toString() {
		return "ProjectInfoCriteria [lname=" + lname + ", pid=" + pid
				+ ", level=" + level + ", isOnline=" + isOnline
				+ ", needSubProject=" + needSubProject + ", needCost="
				+ needCost + ", needImage=" + needImage + "]";
	}

}
