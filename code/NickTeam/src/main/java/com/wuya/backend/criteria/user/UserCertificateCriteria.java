package com.wuya.backend.criteria.user;

import com.wuya.base.criteria.BaseCriteria;

/**
 * [用户证件-条件封装]
 * 
 * @author nick
 * @version v1.0 2015-3-6
 */
public class UserCertificateCriteria extends BaseCriteria {

	/**
	 * [对象序列化标识]
	 */
	private static final long serialVersionUID = -8474200176441257851L;

	/**
	 * [证件类型]
	 */
	private String type;

	/**
	 * [对应通行证标识]
	 */
	private Integer passportId;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Integer getPassportId() {
		return passportId;
	}

	public void setPassportId(Integer passportId) {
		this.passportId = passportId;
	}

	@Override
	public String toString() {
		return "UserCertificateCriteria [type=" + type + ", passportId="
				+ passportId + "]";
	}

}
