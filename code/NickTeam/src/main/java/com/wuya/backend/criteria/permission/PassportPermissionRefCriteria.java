package com.wuya.backend.criteria.permission;

import com.wuya.base.criteria.BaseCriteria;

/**
 * [通行证-权限-关联-条件封装]
 * 
 * @author nick
 * @version v1.0 2015-3-14
 */
public class PassportPermissionRefCriteria extends BaseCriteria {

	/**
	 * [对象序列化标识]
	 */
	private static final long serialVersionUID = 4878461471415739240L;

	/**
	 * [通行证标识]
	 */
	private Integer passportId;

	/**
	 * [所属领域]
	 */
	private String domain;

	public Integer getPassportId() {
		return passportId;
	}

	public void setPassportId(Integer passportId) {
		this.passportId = passportId;
	}

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}

	@Override
	public String toString() {
		return "PassportPermissionRefCriteria [passportId=" + passportId
				+ ", domain=" + domain + "]";
	}

}
